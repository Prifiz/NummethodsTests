package initials;

public class AbstractRectDistributionGenerator {
    private double left;
    private double right;
    private double top;
    private double bottom;

    public AbstractRectDistributionGenerator(double left, double right, double top, double bottom) {
        this.left = left;
        this.right = right;
        this.top = top;
        this.bottom = bottom;
    }
}
